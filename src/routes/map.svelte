		progressive ». Les deux lycées vont bénéficier d'aménagements pour leur première année sur la
		plate-forme. Tous les collégiens parisiens pourront candidater à Henri-IV et à Louis-le-Grand
		sans distinction de secteur. Un quota sur la base de l'indice de position sociale des collèges
		d'origine – de l'ordre d'un tiers de collèges défavorisés, un tiers d'intermédiaires, un tiers
		de classes favorisées – est également à l'étude.
	</p>
	<p>
