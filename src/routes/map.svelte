<div class="container">
	<p>
		C'est une petite révolution qui se prépare sur la montagne Sainte-Geneviève, dans le 5e
		arrondissement de Paris. Les lycées Henri-IV et Louis-le-Grand ne recruteront plus leurs élèves
		de seconde parisiens sur dossier mais à travers la procédure informatisée Affelnet, a appris Le
		Monde. Les deux établissements prestigieux perdent ainsi leur régime d'exception.
	</p>
	<p>
		Sous l'impulsion du recteur, Christophe Kerrero, ancien directeur de cabinet de Jean-Michel
		Blanquer, l'académie de Paris mène depuis 2020 une réforme d'ampleur du système d'affectation
		des 15 000 élèves de troisième dans 45 lycées publics de la capitale, avec une nouvelle
		sectorisation plus fine, autour de cinq établissements à privilégier par collège et un indice de
		position sociale (IPS), qui donne des points bonus au collège en fonction des classes
		socioprofessionnelles des parents.
	</p>
	<p>
		L'intégration des deux lycées d'élite à ce logiciel lancé en 2008 vient apporter une brique
		supplémentaire à ces évolutions. « La réforme dans son ensemble a pour objectif d'apporter plus
		de transparence et plus d'équité. Henri-IV et Louis-le-Grand possèdent une forte visibilité et
		font figure de référence au niveau national. Ils ont vocation à montrer la voie en termes
		d'ouverture sociale », affirme Claire Mazeron, la directrice académique des services de
		l'éducation nationale (Dasen) de Paris chargée des lycées.
	</p>
	<p>
		Malgré leur participation à des cordées de la réussite, un dispositif d'accompagnement des
		élèves défavorisés dans leur orientation, les deux établissements comptent aujourd'hui moins de
		9 % de boursiers en seconde contre près de 24 % pour l'ensemble de l'académie.
	</p>
	<p>Un certain mécontentement dans les deux établissements</p>
	<p>
		Pour « préserver un équilibre », cette intégration dans Affelnet doit se faire « de manière
		progressive ». Les deux lycées vont bénéficier d'aménagements pour leur première année sur la
		plate-forme. Tous les collégiens parisiens pourront candidater à Henri-IV et à Louis-le-Grand
		sans distinction de secteur. Un quota sur la base de l'indice de position sociale des collèges
		d'origine – de l'ordre d'un tiers de collèges défavorisés, un tiers d'intermédiaires, un tiers
		de classes favorisées – est également à l'étude.
	</p>
	<p>
		Il n'empêche. La communauté éducative des deux établissements ne voit pas la réforme d'un bon
		œil. Des parents de collégiens ont appris cette intégration en consultant le site de l'académie
		début janvier et, depuis, l'information se répand comme une traînée de poudre. Parents, élèves
		et enseignants affichent leur mécontentement. Sur la forme tout d'abord. « Il aurait été normal
		que l'on soit consulté », s'insurge Léonard Im, élu au conseil de la vie lycéenne de
		Louis-le-Grand. « On ne s'y attendait pas du tout. Ce n'était pas à l'ordre du jour jusqu'à
		présent », abonde Laurent Lemaire, professeur de mathématiques à Henri-IV.
	</p>
	<p>
		Le recours à un algorithme, qui devient la norme en France pour l'entrée au lycée comme pour
		l'accès à l'enseignement supérieur, est déploré. « Il est important que les enseignants
		s'impliquent et regardent les dossiers scolaires, les notes mais aussi les appréciations, pour
		voir de qui on va pouvoir obtenir le meilleur. Cet élitisme scolaire fonctionne. Il n'y a qu'à
		voir le nombre d'élèves que nous plaçons aux concours généraux », met en avant Anne Paradas,
		enseignante en mathématiques et responsable du SNES-FSU à Louis-le-Grand. « Affelnet est une
		usine à gaz et ne détectera pas certains profils atypiques comme aujourd'hui », affirme de son
		côté Leonard Im.
	</p>
	<p>La fin de l'« excellence » ?</p>
	<p>
		Parents, enseignants et élèves craignent une baisse du niveau scolaire, et avec elle de «
		l'excellence à la française ». « Le traitement algorithmique ne permet pas de distinguer les
		bons, les très bons et les excellents élèves, s'inquiète Antoine Bonneval, responsable de la
		FCPE à Louis-le-Grand. On ne cherche pas à défendre la bourgeoisie parisienne mais l'excellence
		au sein du service public. »
	</p>
	<p>
		« Les informations sont encore parcellaires. On ne sait pas comment les critères vont être gérés
		», tempère Catherine Roux, la représentante de la PEEP à Louis-le-Grand, mais il est important
		de « ne pas perdre ce sur-mesure qui fait partie de l'identité de nos lycées ». Beaucoup y
		voient un « cadeau fait aux lycées privés » du centre de Paris. « La décision est présentée
		comme une recherche de mixité sociale, mais elle s'attaque à l'excellence publique pour la
		livrer au privé », déplore Anne Paradas.
	</p>
	<p>
		Les familles dénoncent aussi une rupture d'égalité entre les élèves parisiens et non parisiens.
		Les collégiens hors de l'académie de Paris, qui représentent environ 40 % des élèves de seconde
		à Louis-le-Grand et 25 % à Henri-IV, continueront à postuler à travers une procédure sur
		dossier, légèrement rénovée, en 2022.
	</p>
	<p>
		Côté Henri-IV, c'est la transition entre le collège et le lycée qui inquiète. Les classes de
		seconde comptent beaucoup d'élèves de troisième d'Henri-IV – plus de 40 sur 250 élèves à la
		rentrée 2021. Une tradition bien connue qui a conduit des familles à y inscrire leurs enfants
		dès le collège pour maximiser leurs chances d'accéder dans la continuité au lycée. Avec
		l'intégration dans Affelnet, cette proportion risque de baisser drastiquement et pourrait même
		jouer, à terme, sur le marché de l'immobilier parisien, très corrélé à la carte scolaire.
	</p>
	<p>
		Si la communauté éducative des deux lycées s'inquiète, ce n'est pas le cas de l'ensemble des
		familles parisiennes. La FCPE Paris se montre plutôt favorable à l'arrivée des deux prestigieux
		lycées du 5e arrondissement de Paris dans la procédure Affelnet. « Le recrutement privilégié
		entre le collège et le lycée Henri-IV est vécu pour beaucoup comme une injustice, met en avant
		Ghislaine Morvan-Dubois, présidente départementale de la fédération de parents d'élèves. Nous
		demandons depuis longtemps au niveau de l'académie de la transparence et la fin des
		passe-droits. »
	</p>
	<p>Meilleure répartition des boursiers</p>
	<p>
		« L'absence de Louis-le-Grand et d'Henri-IV dans Affelnet pouvait nuire à l'acceptation globale
		du système », juge Julien Grenet, chercheur et président du comité de suivi de la réforme. Avec
		son équipe de recherche de l'Institut des politiques publiques (IPP), l'économiste a mené une
		enquête sur l'impact de cette première année de réforme sur la sociologie des lycées parisiens.
		Les premiers résultats se font sentir : la ségrégation sociale entre les lycées de la capitale
		est en baisse de 33 % par rapport à 2020. En clair, la réforme fait bouger la répartition des
		classes sociales au sein des lycées.
	</p>
	<p>
		Les taux de boursiers ont ainsi évolué dès le premier tour d'affectation. Les lycées les plus
		favorisés comptent davantage de boursiers. Charlemagne, dans le 4e arrondissement, est passé de
		23 % à 28,5 % de boursiers entre 2020 et 2021, Victor-Duruy, dans le 7e arrondissement, de 8,8 %
		à 16,2 %, Sophie-Germain, dans le 4e arrondissement, de 24 % à 29,5 %. Dans le même temps, les
		lycées les moins favorisés ont vu leur proportion de boursiers baisser, à l'instar
		d'Henri-Bergson, dans le 19e arrondissement, passé de 60 % à 39,4 % au premier tour
		d'affectation.
	</p>
	<p>
		La ségrégation scolaire a également diminué de 28 % en un an, selon les chiffres du rectorat.
		Son objectif est de mettre fin aux lycées de niveau. L'écart-type de la note moyenne au brevet
		des élèves de seconde des différents établissements a ainsi baissé de 9 % en un an, rendant le
		niveau scolaire à l'entrée en seconde générale et technologique plus homogène. Exemple : le
		lycée Charlemagne, qui n'accueillait quasiment que des élèves dont la moyenne en troisième
		dépassait les 15 sur 20, compte plus d'un élève sur cinq en seconde avec une moyenne inférieure
		à 15 sur 20 à la rentrée 2021. « C'est l'effet conjugué d'une sectorisation plus fine et du
		bonus accordé aux collèges défavorisés qui conduit à ces résultats d'un point de vue scolaire
		comme social », affirme Julien Grenet. Plus de neuf élèves sur dix ont été affectés à moins de
		vingt-cinq minutes de leur collège de secteur.
	</p>
	<p>
		Sur la base de ces conclusions, l'académie de Paris va encore conduire des ajustements pour la
		session 2022 d'Affelnet. La sectorisation pourrait évoluer à la marge, notamment dans le
		Sud-Ouest parisien, pour aboutir à davantage de mixité. Julien Grenet constate « encore beaucoup
		d'autocensure », alors il en est convaincu : « Dans les années à venir, les comportements vont
		certainement évoluer. » Y compris sur la montagne Sainte-Geneviève.
	</p>
